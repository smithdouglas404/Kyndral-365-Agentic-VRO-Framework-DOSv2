/**
 * SEED AGENT ATTRIBUTES
 *
 * Seeds the agent_attributes table with dynamic attributes for all agents.
 * Each attribute can power a dashboard widget, enabling "liquid" UI.
 *
 * Idempotent - safe to run multiple times (uses upsert pattern)
 */

import { db } from '../db.js';
import { sql } from 'drizzle-orm';

interface AttributeDefinition {
  name: string;
  displayName: string;
  description: string;
  category: string;
  dataType: 'number' | 'percentage' | 'currency' | 'text' | 'date' | 'boolean' | 'array' | 'status';
  unit?: string;
  format?: string;
  valueSource: 'calculated' | 'aggregated' | 'static' | 'api' | 'rulebricks';
  calculationRule?: string;
  aggregationMethod?: string;
  defaultWidgetType: 'stat-card' | 'gauge' | 'chart-line' | 'chart-bar' | 'chart-pie' | 'table' | 'list' | 'progress' | 'status-badge' | 'heat-map' | 'kpi-card' | 'trend-card';
  thresholds?: { warning: number; critical: number };
  palantirPropertyName?: string;
  refreshInterval?: number;
}

