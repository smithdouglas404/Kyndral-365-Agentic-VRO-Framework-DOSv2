// ============================================================================
// SEED FUNCTION
// ============================================================================
export async function seedAgentAttributes() {
  console.log('[Seed] Starting agent attributes seed...');

  let created = 0;
  let updated = 0;

  for (const [agentId, attributes] of Object.entries(AGENT_ATTRIBUTES_MAP)) {
    console.log(`[Seed] Processing ${attributes.length} attributes for agent: ${agentId}`);

    for (const attr of attributes) {
      const attributeId = `${agentId}_${attr.name}`;

      // Check if exists
      const existing = await db.execute(sql`
        SELECT id FROM agent_attributes WHERE id = ${attributeId} LIMIT 1
      `);

      if (existing.rows.length > 0) {
        // Update existing
        await db.execute(sql`
          UPDATE agent_attributes SET
            display_name = ${attr.displayName},
            description = ${attr.description || null},
            category = ${attr.category},
            data_type = ${attr.dataType},
            unit = ${attr.unit || null},
            format = ${attr.format || null},
            value_source = ${attr.valueSource},
            calculation_rule = ${attr.calculationRule || null},
            aggregation_method = ${attr.aggregationMethod || null},
            default_widget_type = ${attr.defaultWidgetType},
            thresholds = ${attr.thresholds ? JSON.stringify(attr.thresholds) : null},
            palantir_property_name = ${attr.palantirPropertyName || null},
            refresh_interval = ${attr.refreshInterval || 300},
            updated_at = CURRENT_TIMESTAMP
          WHERE id = ${attributeId}
        `);
        updated++;
      } else {
        // Insert new
        await db.execute(sql`
          INSERT INTO agent_attributes (
            id, agent_id, name, display_name, description, category,
            data_type, unit, format, value_source, calculation_rule,
            aggregation_method, default_widget_type, thresholds,
            palantir_property_name, refresh_interval
          ) VALUES (
            ${attributeId},
            ${agentId},
            ${attr.name},
            ${attr.displayName},
            ${attr.description || null},
            ${attr.category},
            ${attr.dataType},
            ${attr.unit || null},
            ${attr.format || null},
            ${attr.valueSource},
            ${attr.calculationRule || null},
            ${attr.aggregationMethod || null},
            ${attr.defaultWidgetType},
            ${attr.thresholds ? JSON.stringify(attr.thresholds) : null},
            ${attr.palantirPropertyName || null},
            ${attr.refreshInterval || 300}
          )
        `);
        created++;
      }
    }
  }

  // Count totals
  const totalCount = await db.execute(sql`SELECT COUNT(*) as count FROM agent_attributes`);
  const total = totalCount.rows[0]?.count || 0;

  console.log(`[Seed] Agent attributes: ${created} created, ${updated} updated, ${total} total`);
  console.log('[Seed] Agent attributes seed complete!');

  return { created, updated, total };
}

// Run if called directly
if (process.argv[1]?.includes('seed-agent-attributes')) {
  seedAgentAttributes()
    .then((result) => {
      console.log('[Seed] Result:', result);
      process.exit(0);
    })
    .catch((err) => {
      console.error('[Seed] Error:', err);
      process.exit(1);
    });
}
